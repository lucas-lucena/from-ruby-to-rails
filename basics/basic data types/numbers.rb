# Numbers
# Addition
1 + 1 #=> 2

# Subtraction
2 - 1 #=> 1

# Multiplication
3 * 2 #=> 6

# Division
20 / 4 #=> 5

# Exponent
10 ** 2 #=> 100
8 ** 3 #=> 512 

# Modulus (find the remainder of division)
18 % 5 #=> 3
3 % 2 #=> 1

# Floats
5 / 2 #=> 2, not 2.5

5 / 2.0 #=> 2.5

# Converting numbers types

# To convert an integer to a float:
13.to_f #=> 13.0

# To convert a float to an integer:
13.0.to_i #=> 13
13.3.to_i #=> 13

# Some useful number methods

# even?
6.even? #=> true
7.even? #=> false

# odd?
6.odd? #=> false
7.odd? #=> true


